# Now we'll learn the conversion of one datatype to another datatype.
a = int(5.3)        
b = int('187')      
c = bool(10)
d = bool(-10)
e = bool(0)
j = bool('0')
print(a,type(a))      #<class 'int'>
print(b,type(b))      #<class 'int'>
print(c,type(c))      #True <class 'bool'>
print(d,type(d))      #True <class 'bool'>
print(e,type(e))      #False <class 'bool'>
print(j,type(j))      #True <class 'bool'>




